 <legend>Dark Pool</legend>

The Mt Gox dark pool allows you to buy or sell large quantities of BTC without moving the market.
<p>
You can mark any buy or sell order with a value over $1000 as being in the dark pool. 
<p>
Orders in the dark pool don't show up on the order book.
<p>
There are 2 types of dark pool orders. <br>
 <b>Dark pool and Normal</b> - Can be filled either partially from the normal orders or the dark pool.  
<br> <b>Dark pool Only</b> - Can be filled only by other dark pool orders or a single normal order that is larger than the dark pool order. This means that if there is a single normal order that would fill the dark pool order both will be filled.
 
