<h2>How Payment Services Works</h2>

<fieldset>
 <legend>Payment Button</legend>
You can use the Mt. Gox Payment Button to easily accept Bitcoin payment on your website. It is quick and easy to setup. If your customer happens to have a Mt. Gox account the payment will be instant. Otherwise it will take ~1 hour for the Bitcoin network to complete the transaction.
<p>
<ul>
	<li>Easy Setup </li>
	<li>Some payments will be instant</li>
	<li>Don't need to integrate with or even run bitcoind</li>
</ul> 
</fieldset>

<fieldset>
<legend>Simple Setup</legend>
The quickest way to get started:
<ul>
	<li><a href="/merch/widget">Get your Widget Code.</a></li>
	<li>Paste it into your website.</li>
	<li>Fill out the amount option to set how many BTC the customer will be charged.</li>
	<li>Fill out the custom field however you want.</li>
	<li>Done!</li>
	<li><a href="/merch">Merchant Services</a> will list all payments you have received.</li>
</ul> 
</fieldset>

<fieldset>
 <legend>Advanced Callback Setup</legend>
If you want to be notified any time a user successfully sends you payment. This method requires you to set up a call back url that we hit to notify you there has been a transaction.
<ul>
	<li><a href="/merch/widget">Get your Widget Code.</a></li>
	<li>Paste it into your website.</li>
	<li>Fill out the amount option to set how many BTC the customer will be charged.</li>
	<li>Fill out the custom field however you want.</li>
	<li>Go to <a href="/users/settings">your settings</a> and fill out the Payment Notification URL to be the url you want us to call when there is a payment.</li>
	<li>Create that Payment Callback. <a href="/merch/cb_example">See an example</a></li>
	<li>Done!</li>
	<li><a href="/merch">Merchant Services</a> will list all payments you have received.</li>
</ul> 
</fieldset>


